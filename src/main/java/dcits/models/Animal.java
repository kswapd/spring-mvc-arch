public class Animal {
	private String animalName;
	private int age;
	private String id;


	public String getAnimalName() {
		return animalName;
	}
	public void setAnimalName(String name) {
		this.animalName = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}


}
